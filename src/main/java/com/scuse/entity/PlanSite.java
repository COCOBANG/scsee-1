package com.scuse.entity;

public class PlanSite {
    private Integer plnId;

    private Integer siteId;

    private Integer candLimit;

    public Integer getPlnId() {
        return plnId;
    }

    public void setPlnId(Integer plnId) {
        this.plnId = plnId;
    }

    public Integer getSiteId() {
        return siteId;
    }

    public void setSiteId(Integer siteId) {
        this.siteId = siteId;
    }

    public Integer getCandLimit() {
        return candLimit;
    }

    public void setCandLimit(Integer candLimit) {
        this.candLimit = candLimit;
    }
}